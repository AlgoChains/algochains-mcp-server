export interface Profile {
  mode: "demo" | "paper" | "live";
  tool_mode: "smart" | "full";
  broker?: string;
  daily_loss_limit?: number;
  require_confirm?: boolean;
  mcp_bridge_url?: string;
}

export interface CliConfig {
  default: { profile: string };
  profile: Record<string, Profile>;
  repl: {
    vim_mode: boolean;
    theme: "dark" | "light";
    regime_refresh_interval_ms: number;
    history_limit: number;
  };
  daemon: {
    port: number;
    auto_start: boolean;
  };
  telemetry: {
    enabled: boolean;
    endpoint?: string;
  };
  mcp: {
    bridge_url: string;
    timeout_ms: number;
  };
}

