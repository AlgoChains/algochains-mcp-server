export function getActiveProfile(profileName?: string, config?: CliConfig): Profile {
  const cfg = config ?? loadConfig();
  const name = profileName ?? cfg.default.profile;
  return cfg.profile[name] ?? DEFAULT_CONFIG.profile.demo;
}

function deepMerge(base: Record<string, unknown>, override: Record<string, unknown>): Record<string, unknown> {
  const result = { ...base };
  for (const [k, v] of Object.entries(override)) {
    if (v && typeof v === "object" && !Array.isArray(v) && typeof result[k] === "object") {
      result[k] = deepMerge(result[k] as Record<string, unknown>, v as Record<string, unknown>);
    } else if (v !== undefined) {
      result[k] = v;
    }
  }
  return result;
}
